# Variabili e costanti sono alias che utilizziamo per riferirci ad un dato
#
# PI = 3.14 # costante, sempre in maiuscolo
# age = 30 # variabile, sempre in minuscolo
#
# age è il nome della variabile
# = è l'operazione di assegnazione
# 30 è il valore assegnato alla variabile
#
# Python in realtà non distingue tra variabile e costante
# si usa una convenzione per distinguerle
# le COSTANTI si scrivono in MAIUSCOLO
# le variabili si scrivono in minuscolo
#
# il contenuto di una costante non può cambiare
# le variabili possono cambiare il loro contenuto
#
# i nomi delle variabili e delle costanti devono rispecchiare il loro contenuto
#
# in python i nomi delle variabili che sono formati da più parole, seguono la seguente sintassi
# snake case => consiste nel separare le parole con un trattino basso
# last_name, first_number ...
# I TIPI DI DATI
#
# NOTA - IN PYTHON TUTTI I TIPI DI DATI SONO OGGETTI
#
# i tipi di dati messi a disposizione da python sono:
#
# int   - numeri interi
# float - numeri con la virgola - è in doppia precisione come il double
# str   - stringhe di testo     - può rappresentare anche un singolo char
# bool  - valori booleani
# byte  - valori binari
#
# DIMENSIONE IN BYTE PER I TIPI DI DATI
# int   28 byte
# float 24 byte
# str   50 + 1 byte per ogni carattere
# bool  28 byte
# bytes  34 + 1 byte per ogni bit
#
# Python non è un linguaggio memory efficient (non è ottimizzato per l'utilizzo della memoria)
# infatti con grande quantità di dati ci potrebbe essere qualche problemino, es. big data
#

# le espressioni booleane ci permettono di fare dei confronti attraverso gli operatori booleani
# in questo caso il > è l'operatore booleano e l'espressione restituirà True
print("5 > 3 = ", 5 > 3)

# principali operatori booleani

#  ==   True se i due valori sono uguali, False altrimenti
#  !=   True se i due valori sono diversi, False altrimenti
#  >    True se il primo valore è maggiore del secondo, False altrimenti
#  >=   True se il primo valore è maggiore uguale del secondo, False altrimenti
#  <    True se il primo valore è minore del secondo, False altrimenti
#  <=   True se il primo valore è minore uguale del secondo, False altrimenti


# Operatori logici
# sono operatori che permettono di unire + espressioni booleane

# ES.  1==1 and "gatto" != "cane"

print("(1==1 and gatto != cane) = ", 1 == 1 and "gatto" != "cane")


# Area della circonferenza
# Definisci una funzione che, prendendo in input il raggio,
# restituisce l'area della circonferenza.
#

def compute_area_circle(r):
    pi = 3.14
    return pi*r*r


# raggio = int(input("Digita il raggio della circonferenza "))
raggio = 5
print(compute_area_circle(raggio))

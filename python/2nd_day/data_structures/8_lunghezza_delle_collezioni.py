# Per tutti i tipi di collezione python mette a disposione
# la funzione len() per conoscere la lunghezza della collezione

# Lista
lista = [1, 2, 3, 4]
print(len(lista))

# Tupla
tu = (1, 2, 3, 4, 5, 6)
print(len(tu))

# Set
insieme = {5, 8, 9, 7, 6, 4, 2}
print(len(insieme))

# Dictionary
mappa = {"1": 1, "2": 2, "3": 3}
print(len(mappa))

